package net.imagej.ops;

import java.util.ArrayList;
import java.util.List;

import org.scijava.plugin.AbstractPTService;
import org.scijava.plugin.Parameter;
import org.scijava.plugin.Plugin;
import org.scijava.plugin.PluginService;
import org.scijava.service.Service;

/**
 * Default service for reducing an {@link OpRef} to other {@link OpRef}s.
 *
 * @author Curtis Rueden
 */
@Plugin(type = Service.class)
public class DefaultOpReduceService extends AbstractPTService<OpReducer>
	implements OpReduceService
{

	@Parameter
	private PluginService pluginService;

	@Override
	public List<OpRef<?>> reduce(final OpRef<?> ref) {
		final List<OpReducer> reducers = //
			pluginService.createInstancesOfType(OpReducer.class);
		final ArrayList<OpRef<?>> refs = new ArrayList<>();
		for (final OpReducer reducer : reducers) {
			refs.addAll(reducer.reduce(ref));
		}
		return refs;
	}

}
